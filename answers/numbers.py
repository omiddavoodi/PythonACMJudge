def namayesh(lis):
    nama = ''
    for j in range(6):
        for i in range(len(lis[0])):
            nama += lis[j][i]
            #nama += '*'
        if j != 5:
            nama += "\n"
    return nama

def make_list(length):
    lis=[]
    for i in range(6):
        lis.append([])
        for j in range(length):
            lis[i].append(" ")
    return lis

